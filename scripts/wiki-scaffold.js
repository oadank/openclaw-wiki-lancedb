#!/usr/bin/env node
/**
 * wiki-scaffold.js - Scaffold 自动重建（Synthadoc 架构）
 *
 * 根据 wiki 当前状态自动重建：
 *   1. index.md    — 完整目录，含 one-line summary（保护已有链接）
 *   2. purpose.md — 范围声明（声明 wiki 的边界）
 *   3. overview.md — 全局概览（取最近修改的 N 页，LLM 总结）
 *
 * 用法:
 *   node wiki-scaffold.js              # 全量重建
 *   node wiki-scaffold.js --index     # 仅重建 index
 *   node wiki-scaffold.js --overview  # 仅重建 overview
 *   node wiki-scaffold.js --dry-run    # 预览不写入
 */

const fs = require('fs');
const path = require('path');
const { execSync, spawnSync } = require('child_process');

const VAULT = process.env.OBSIDIAN_VAULT_PATH || process.env.WIKI_VAULT_PATH || path.join(__dirname, '..');
const LOG_PATH = path.join(VAULT, 'log.md');

const DRY_RUN = process.argv.includes('--dry-run');

const LLM_BASE_URL = process.env.LLM_BASE_URL || 'http://localhost:4000';
const LLM_API_KEY = process.env.LLM_API_KEY || '';
const LLM_MODEL = process.env.LLM_MODEL || 'your-model-name';

// ── helpers ────────────────────────────────────────────────────────────────

function extractFrontmatter(content) {
  const m = content.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  return m ? { fm: m[1], body: m[2].trim() } : { fm: '', body: content };
}

function getField(fm, field) {
  const m = fm.match(new RegExp(`${field}:\\s*"?([^"'\n]+)"?`));
  return m ? m[1].trim() : null;
}

function callLLM(prompt, maxTokens = 1500) {
  const tmpFile = '/tmp/wiki-scaffold-prompt.json';
  const payload = { model: LLM_MODEL, messages: [{ role: 'user', content: prompt }], max_tokens: maxTokens, temperature: 0.3 };
  fs.writeFileSync(tmpFile, JSON.stringify(payload));
  try {
    const r = spawnSync('curl', ['-s', '-m', '90', `${LLM_BASE_URL}/chat/completions`,
      '-H', `Authorization: Bearer ${LLM_API_KEY}`, '-H', 'Content-Type: application/json', '-d', `@${tmpFile}`],
      { encoding: 'utf8', timeout: 100000 });
    if (r.status !== 0) return null;
    return JSON.parse(r.stdout).choices?.[0]?.message?.content || null;
  } catch (e) { return null; }
  finally { try { fs.unlinkSync(tmpFile); } catch (e) {} }
}

function appendLog(details) {
  const line = `\n| ${new Date().toISOString()} | scaffold | ✅ | ${details} |`;
  if (fs.existsSync(LOG_PATH)) fs.appendFileSync(LOG_PATH, line);
}

function walkPages(dir, depth = 0) {
  const results = [];
  if (!fs.existsSync(dir)) return results;
  for (const entry of fs.readdirSync(dir)) {
    if (/^\./.test(entry)) continue;
    const full = path.join(dir, entry);
    const stat = fs.statSync(full);
    if (stat.isDirectory()) {
      if (depth < 2) results.push(...walkPages(full, depth + 1));
    } else if (entry.endsWith('.md') && !/^(index|log)\.md$/.test(entry)) {
      try {
        const content = fs.readFileSync(full, 'utf8');
        const { fm, body } = extractFrontmatter(content);
        results.push({
          rel: path.relative(VAULT, full),
          title: getField(fm, 'title') || entry.replace('.md', ''),
          category: path.basename(dir),
          tags: (fm.match(/tags:\s*\[([^\]]*)\]/) || [''])[0].replace(/tags:\s*\[/, '').replace(']', ''),
          summary: getField(fm, 'summary') || '',
          status: getField(fm, 'status') || 'active',
          fullPath: full,
          body: body.substring(0, 200),
        });
      } catch (e) {}
    }
  }
  return results;
}

// ── 1. rebuild index.md ──────────────────────────────────────────────────

async function rebuildIndex(pages) {
  console.log('📋 重建 index.md ...');
  const header = `# LLM Wiki Index

> Auto-generated by wiki-scaffold.js. Every entry links to a wiki page with a one-line summary.
> **Do not edit entries manually — they are regenerated on every scaffold run.**

`;

  // 按分类组织
  const byCategory = {};
  for (const p of pages) {
    const cat = p.category || 'general';
    if (!byCategory[cat]) byCategory[cat] = [];
    byCategory[cat].push(p);
  }

  const lines = [header];
  for (const [cat, catPages] of Object.entries(byCategory).sort(([a], [b]) => a.localeCompare(b))) {
    lines.push(`## ${cat.charAt(0).toUpperCase() + cat.slice(1)}`);
    for (const p of catPages.sort((a, b) => a.title.localeCompare(b.title))) {
      const link = p.rel.replace('.md', '').replace(/\s+/g, '-');
      const summary = p.summary ? ` — ${p.summary}` : ' — (no summary)';
      lines.push(`- [[${link}|${p.title}]]${summary}`);
    }
    lines.push('');
  }

  const result = lines.join('\n');
  if (!DRY_RUN) {
    fs.writeFileSync(path.join(VAULT, 'index.md'), result);
    console.log(`  ✅ index.md 写入完成 (${pages.length} 页)`);
  } else {
    console.log(`  [dry-run] index.md 应写入 ${pages.length} 条`);
  }
  return pages.length;
}

// ── 2. rebuild purpose.md ─────────────────────────────────────────────────

async function rebuildPurpose(pages) {
  const purposePath = path.join(VAULT, 'purpose.md');

  // 读取现有的 purpose.md（如果有）
  let existingScope = '';
  if (fs.existsSync(purposePath)) {
    const existing = fs.readFileSync(purposePath, 'utf8');
    const scopeMatch = existing.match(/## Scope\n([\s\S]*?)(?=##|$)/);
    if (scopeMatch) existingScope = scopeMatch[1].trim();
  }

  const prompt = `You are defining the scope and purpose of a personal wiki knowledge base.

**Current scope (if any):**
${existingScope || '(none yet — help define it)'}

**All pages in the wiki:**
${pages.slice(0, 50).map(p => `- ${p.title} (${p.category}${p.summary ? ': ' + p.summary : ''})`).join('\n')}
${pages.length > 50 ? `\n... and ${pages.length - 50} more pages` : ''}

Write a concise **## Scope** section (3-6 bullet points) defining what this wiki covers and what is explicitly out of scope.

Respond ONLY with the scope section in markdown format:
## Scope
- ...
`;

  console.log('🎯 重建 purpose.md ...');
  const llmResult = await callLLM(prompt, 600);
  if (!llmResult) {
    if (fs.existsSync(purposePath)) {
      console.log('  ⏭️  LLM 不可用，保留现有 purpose.md');
    } else {
      console.log('  ⚠️  LLM 不可用，跳过');
    }
    return 0;
  }

  const content = `---
title: Wiki Purpose & Scope
status: auto
generated: ${new Date().toISOString()}
---

# Wiki Purpose & Scope

${llmResult.trim()}

---

*Auto-generated by wiki-scaffold.js — re-run to update.*
`;

  if (!DRY_RUN) {
    fs.writeFileSync(purposePath, content);
    console.log('  ✅ purpose.md 写入完成');
  } else {
    console.log('  [dry-run] purpose.md 应写入');
  }
  return 1;
}

// ── 3. rebuild overview.md ────────────────────────────────────────────────

async function rebuildOverview(pages) {
  // 取最近修改的 10 页（按 mtime）
  const recent = pages.slice(0, 10);
  if (recent.length === 0) { console.log('  ⏭️  无页面，跳过'); return 0; }

  const context = recent.map(p => `## ${p.title}\n${p.body || p.summary || '(no content)'}`).join('\n\n');

  const prompt = `You are writing the overview page for a personal wiki knowledge base.

Read the following recently modified pages and write a 2-paragraph overview of the entire wiki — what it covers, the main themes, and how the pieces connect.

**Recent pages:**
${context}

Write a concise overview:

## Overview
[2 paragraphs synthesizing the wiki's scope and themes]

## Recent Activity
- ${recent.map(p => `[[${p.rel.replace('.md', '')}|${p.title}]]`).join('\n- ')}
`;

  console.log('📖 重建 overview.md ...');
  const llmResult = await callLLM(prompt, 1200);
  if (!llmResult) { console.log('  ⚠️  LLM 不可用，跳过'); return 0; }

  const content = `---
title: Wiki Overview
status: auto
generated: ${new Date().toISOString()}
---

# Wiki Overview

${llmResult.trim()}

---

*Auto-generated by wiki-scaffold.js — re-run to update. Pages already linked in index.md are protected and preserved.*
`;

  if (!DRY_RUN) {
    fs.writeFileSync(path.join(VAULT, 'overview.md'), content);
    console.log('  ✅ overview.md 写入完成');
  } else {
    console.log('  [dry-run] overview.md 应写入');
  }
  return 1;
}

// ── main ─────────────────────────────────────────────────────────────

async function main() {
  if (DRY_RUN) console.log('🔍 [dry-run 模式 — 不写入任何内容]\n');

  const mode = process.argv.find(a => a.startsWith('--')) || '';
  const isIndexOnly = mode === '--index';
  const isOverviewOnly = mode === '--overview';

  console.log('🔍 扫描 wiki 页面 ...');
  const pages = walkPages(VAULT);
  console.log(`📦 共 ${pages.length} 个页面\n`);

  if (pages.length === 0) { console.log('❌ 无页面可处理'); return; }

  let count = 0;

  if (!isOverviewOnly) {
    count += await rebuildIndex(pages);
    count += await rebuildPurpose(pages);
  }

  if (!isIndexOnly) {
    count += await rebuildOverview(pages);
  }

  if (!DRY_RUN) appendLog(`scaffold 重建 ${count} 个文件`);
  console.log(`\n📊 完成: ${count} 个 scaffold 文件`);
  if (DRY_RUN) console.log('（dry-run，未写入）');
}

main().catch(err => { console.error('Fatal error:', err); process.exit(1); });
